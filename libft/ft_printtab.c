#include "libft.h"

void	ft_printtab(char **ta)
{
	int		i;

	i = 0;
	while (ta[i])
	{
		ft_putstr("tab[");
		ft_putnbr(i);
		ft_putstr("]: ");
		ft_putendl(ta[i]);
		i++;
	}
}
